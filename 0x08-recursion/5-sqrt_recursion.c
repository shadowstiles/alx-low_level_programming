/**
 * _sqrt_recursion - To find the square of a number
 * @n: The number
 * Return: 0 || -1
 */

int _sqrt_recursion(int n)
{
	int c;

	c = count(n);

	return (c);
}

int count(int n)
{
	int i;

	for (i = 1; i < 9 && i > 0; i++)
	{
		if (i * i = n)
			return (i * i);
	}

	return (-1);
}
